package OOP.Session20;

public class StaticAccessModifiers {

    // STATIC
    // static modifier poate sa fie aplicat la nivel de variabila, metoda, block de initializare, inner class ori la inner interface
    // static keyword poate fi apelat prin numele clasei (numeClasa.metoda / numeClasa.field)
    // eficientizeaza folosirea memoriei


    static String name = "Endava";

    public static void printMessage() {
        System.out.println("Printeaza metoda.");
    }


}


